local syntax = require "core.syntax"

syntax.add {
  files = "%.lua$",
  headers = "^#!.*[ /]lua",
  comment = "--",
  patterns = {
    { pattern = { '"', '"', '\\' },       type = "string"   },
    { pattern = { "'", "'", '\\' },       type = "string"   },
    { pattern = { [=[\[\[]=], [=[\]\]]=] }, type = "string" },
    { pattern = { [=[--\[\[]=], [=[\]\]]=] }, type = "comment" },
    { pattern = [=[--.*?\n]=],           type = "comment"  },
    { pattern = [=[-?0x[A-Fa-f0-9]+]=],  type = "number"   },
    { pattern = [=[-?\d+[\d.eE]*]=],    type = "number"   },
    { pattern = [=[-?\.?\d+]=],         type = "number"   },
    { pattern = [=[<[A-Za-z]+>]=],       type = "keyword2" },
    { pattern = [=[\.\.\.?]=],        type = "operator" },
    { pattern = "[<>~=]=",                type = "operator" },
    { pattern = [=[[+\-=/\*^%#<>]]=],    type = "operator" },
    { pattern = [=[[A-Za-z_][A-Za-z0-9_]*\s*(?=[("{])]=], type = "function" },
    { pattern = [=[[A-Za-z_][A-Za-z0-9_]*]=], type = "symbol" },
    { pattern = [=[::[A-Za-z_][A-Za-z0-9_]*::]=], type = "function" },
  },
  symbols = {
    ["if"]       = "keyword",
    ["then"]     = "keyword",
    ["else"]     = "keyword",
    ["elseif"]   = "keyword",
    ["end"]      = "keyword",
    ["do"]       = "keyword",
    ["function"] = "keyword",
    ["repeat"]   = "keyword",
    ["until"]    = "keyword",
    ["while"]    = "keyword",
    ["for"]      = "keyword",
    ["break"]    = "keyword",
    ["return"]   = "keyword",
    ["local"]    = "keyword",
    ["in"]       = "keyword",
    ["not"]      = "keyword",
    ["and"]      = "keyword",
    ["or"]       = "keyword",
    ["goto"]     = "keyword",
    ["self"]     = "keyword2",
    ["true"]     = "literal",
    ["false"]    = "literal",
    ["nil"]      = "literal",
  },
}
