local syntax = require "core.syntax"

syntax.add {
  files = "%.go$",
  comment = "//",
  patterns = {
    { pattern = "//.-\n",               type = "comment" },
    { pattern = { "/%*", "%*/" },       type = "comment" },
    { pattern = { '"', '"', '\\' },     type = "string" },
    { pattern = { "`", "`" },            type = "string" },
    { pattern = { "'", "'", "\\" },     type = "string" },
    { pattern = "0[bB][01_]+",            type = "number" },
    { pattern = "0[oO][0-7_]+",           type = "number" },
    { pattern = "-?0[xX][%x_]+i?",        type = "number" },
    { pattern = "-?%d[%d_]*%.?[%d_]*[eE]?[-+]?%d*i?", type = "number" },
    { pattern = ":=",                     type = "operator" },
    { pattern = "[%+%-=/%*%^%%<>!~|&:]", type = "operator" },
    { pattern = "[%a_][%w_]*%f[(]",      type = "function" },
    { pattern = "[%a_][%w_]*",           type = "symbol" },
  },
  symbols = {
    ["break"] = "keyword", ["case"] = "keyword", ["chan"] = "keyword",
    ["const"] = "keyword", ["continue"] = "keyword", ["default"] = "keyword",
    ["defer"] = "keyword", ["else"] = "keyword", ["fallthrough"] = "keyword",
    ["for"] = "keyword", ["func"] = "keyword", ["go"] = "keyword",
    ["goto"] = "keyword", ["if"] = "keyword", ["import"] = "keyword",
    ["interface"] = "keyword", ["map"] = "keyword", ["package"] = "keyword",
    ["range"] = "keyword", ["return"] = "keyword", ["select"] = "keyword",
    ["struct"] = "keyword", ["switch"] = "keyword", ["type"] = "keyword",
    ["var"] = "keyword", ["int"] = "keyword2", ["int8"] = "keyword2",
    ["int16"] = "keyword2", ["int32"] = "keyword2", ["int64"] = "keyword2",
    ["uint"] = "keyword2", ["uint8"] = "keyword2", ["uint16"] = "keyword2",
    ["uint32"] = "keyword2", ["uint64"] = "keyword2", ["uintptr"] = "keyword2",
    ["float32"] = "keyword2", ["float64"] = "keyword2", ["complex64"] = "keyword2",
    ["complex128"] = "keyword2", ["byte"] = "keyword2", ["rune"] = "keyword2",
    ["string"] = "keyword2", ["bool"] = "keyword2", ["error"] = "keyword2",
    ["true"] = "literal", ["false"] = "literal", ["nil"] = "literal",
  },
}
