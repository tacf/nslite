local syntax = require "core.syntax"

syntax.add {
  files = "%.rs$",
  comment = "//",
  patterns = {
    { pattern = "//.-\n",               type = "comment" },
    { pattern = { "/%*", "%*/" },       type = "comment" },
    { pattern = { 'r#"', '"#' },          type = "string" },
    { pattern = { '"', '"', '\\' },     type = "string" },
    { pattern = "'.'",                    type = "string" },
    { pattern = "'[%a_][%w_]*",           type = "keyword2" },
    { pattern = "0[bB][01_]+",            type = "number" },
    { pattern = "0[oO][0-7_]+",           type = "number" },
    { pattern = "-?0[xX][%x_]+",          type = "number" },
    { pattern = "-?%d[%d_]*%.?[%d_]*[eE]?[-+]?%d*[fiu%d_]*", type = "number" },
    { pattern = "=>",                     type = "operator" },
    { pattern = "::",                     type = "operator" },
    { pattern = "[%+%-=/%*%^%%<>!~|&?:]", type = "operator" },
    { pattern = "[%a_][%w_]*!%f[%[(]",   type = "function" },
    { pattern = "[%a_][%w_]*%f[(]",      type = "function" },
    { pattern = "[%a_][%w_]*",           type = "symbol" },
  },
  symbols = {
    ["as"] = "keyword", ["async"] = "keyword", ["await"] = "keyword",
    ["break"] = "keyword", ["const"] = "keyword", ["continue"] = "keyword",
    ["crate"] = "keyword", ["dyn"] = "keyword", ["else"] = "keyword",
    ["enum"] = "keyword", ["extern"] = "keyword", ["false"] = "literal",
    ["fn"] = "keyword", ["for"] = "keyword", ["if"] = "keyword",
    ["impl"] = "keyword", ["in"] = "keyword", ["let"] = "keyword",
    ["loop"] = "keyword", ["match"] = "keyword", ["mod"] = "keyword",
    ["move"] = "keyword", ["mut"] = "keyword", ["pub"] = "keyword",
    ["ref"] = "keyword", ["return"] = "keyword", ["Self"] = "keyword",
    ["self"] = "keyword", ["static"] = "keyword", ["struct"] = "keyword",
    ["super"] = "keyword", ["trait"] = "keyword", ["true"] = "literal",
    ["type"] = "keyword", ["unsafe"] = "keyword", ["use"] = "keyword",
    ["where"] = "keyword", ["while"] = "keyword", ["yield"] = "keyword",
    ["bool"] = "keyword2", ["char"] = "keyword2", ["str"] = "keyword2",
    ["String"] = "keyword2", ["i8"] = "keyword2", ["i16"] = "keyword2",
    ["i32"] = "keyword2", ["i64"] = "keyword2", ["i128"] = "keyword2",
    ["isize"] = "keyword2", ["u8"] = "keyword2", ["u16"] = "keyword2",
    ["u32"] = "keyword2", ["u64"] = "keyword2", ["u128"] = "keyword2",
    ["usize"] = "keyword2", ["f32"] = "keyword2", ["f64"] = "keyword2",
    ["None"] = "literal", ["Some"] = "literal", ["Ok"] = "literal",
    ["Err"] = "literal",
  },
}
