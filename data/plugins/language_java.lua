local syntax = require "core.syntax"

syntax.add {
  files = "%.java$",
  comment = "//",
  patterns = {
    { pattern = [=[//.*?\n]=],           type = "comment" },
    { pattern = { [=[/\*]=], [=[\*/]=] }, type = "comment" },
    { pattern = { '"', '"', '\\' },     type = "string" },
    { pattern = { "'", "'", "\\" },     type = "string" },
    { pattern = [=[-?0[xX][A-Fa-f0-9]+]=], type = "number" },
    { pattern = [=[-?\d+[\d.eE]*[fFdDlL]?]=], type = "number" },
    { pattern = "->",                     type = "operator" },
    { pattern = "::",                     type = "operator" },
    { pattern = [=[[+\-=/\*^%<>!~|&?:]]=], type = "operator" },
    { pattern = [=[[A-Za-z_][A-Za-z0-9_]*(?=\()]=], type = "function" },
    { pattern = [=[[A-Za-z_][A-Za-z0-9_]*]=], type = "symbol" },
  },
  symbols = {
    ["abstract"] = "keyword", ["assert"] = "keyword", ["break"] = "keyword",
    ["case"] = "keyword", ["catch"] = "keyword", ["class"] = "keyword",
    ["const"] = "keyword", ["continue"] = "keyword", ["default"] = "keyword",
    ["do"] = "keyword", ["else"] = "keyword", ["enum"] = "keyword",
    ["extends"] = "keyword", ["final"] = "keyword", ["finally"] = "keyword",
    ["for"] = "keyword", ["if"] = "keyword", ["implements"] = "keyword",
    ["import"] = "keyword", ["instanceof"] = "keyword", ["interface"] = "keyword",
    ["native"] = "keyword", ["new"] = "keyword", ["package"] = "keyword",
    ["private"] = "keyword", ["protected"] = "keyword", ["public"] = "keyword",
    ["return"] = "keyword", ["static"] = "keyword", ["strictfp"] = "keyword",
    ["super"] = "keyword", ["switch"] = "keyword", ["synchronized"] = "keyword",
    ["this"] = "keyword", ["throw"] = "keyword", ["throws"] = "keyword",
    ["transient"] = "keyword", ["try"] = "keyword", ["var"] = "keyword",
    ["void"] = "keyword", ["volatile"] = "keyword", ["while"] = "keyword",
    ["yield"] = "keyword", ["record"] = "keyword", ["sealed"] = "keyword",
    ["permits"] = "keyword", ["boolean"] = "keyword2", ["byte"] = "keyword2",
    ["char"] = "keyword2", ["double"] = "keyword2", ["float"] = "keyword2",
    ["int"] = "keyword2", ["long"] = "keyword2", ["short"] = "keyword2",
    ["true"] = "literal", ["false"] = "literal", ["null"] = "literal",
  },
}
