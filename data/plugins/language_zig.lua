local syntax = require "core.syntax"

syntax.add {
  files = "%.zig$",
  comment = "//",
  patterns = {
    { pattern = [=[//.*?\n]=],           type = "comment" },
    { pattern = [=[\\\\.*?\n]=],        type = "string" },
    { pattern = { '"', '"', '\\' },     type = "string" },
    { pattern = { "'", "'", "\\" },     type = "string" },
    { pattern = "0[bB][01_]+",            type = "number" },
    { pattern = "0[oO][0-7_]+",           type = "number" },
    { pattern = [=[0[xX][A-Fa-f0-9_]+]=], type = "number" },
    { pattern = [=[\d[\d_]*\.?[\d_]*[eE]?[-+]?\d*]=], type = "number" },
    { pattern = [=[[+\-=/\*^%<>!~|&.?]]=], type = "operator" },
    { pattern = [=[@[A-Za-z_][A-Za-z0-9_]*]=], type = "function" },
    { pattern = [=[[A-Za-z_][A-Za-z0-9_]*(?=\()]=], type = "function" },
    { pattern = [=[[A-Z][A-Za-z0-9_]*]=], type = "keyword2" },
    { pattern = [=[[A-Za-z_][A-Za-z0-9_]*]=], type = "symbol" },
  },
  symbols = {
    ["fn"] = "keyword", ["asm"] = "keyword", ["break"] = "keyword",
    ["continue"] = "keyword", ["defer"] = "keyword", ["errdefer"] = "keyword",
    ["else"] = "keyword", ["enum"] = "keyword", ["error"] = "keyword",
    ["export"] = "keyword", ["extern"] = "keyword", ["for"] = "keyword",
    ["if"] = "keyword", ["inline"] = "keyword", ["noalias"] = "keyword",
    ["nosuspend"] = "keyword", ["opaque"] = "keyword", ["orelse"] = "keyword",
    ["packed"] = "keyword", ["pub"] = "keyword", ["resume"] = "keyword",
    ["return"] = "keyword", ["struct"] = "keyword", ["suspend"] = "keyword",
    ["switch"] = "keyword", ["test"] = "keyword", ["try"] = "keyword",
    ["union"] = "keyword", ["unreachable"] = "keyword", ["usingnamespace"] = "keyword",
    ["var"] = "keyword", ["while"] = "keyword", ["anytype"] = "keyword2",
    ["bool"] = "keyword2", ["comptime_float"] = "keyword2", ["comptime_int"] = "keyword2",
    ["isize"] = "keyword2", ["usize"] = "keyword2", ["void"] = "keyword2",
    ["f16"] = "keyword2", ["f32"] = "keyword2", ["f64"] = "keyword2",
    ["f80"] = "keyword2", ["f128"] = "keyword2", ["true"] = "literal",
    ["false"] = "literal", ["null"] = "literal", ["undefined"] = "literal",
  },
}
